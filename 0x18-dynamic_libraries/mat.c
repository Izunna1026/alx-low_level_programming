#include <stdio.h>
/**
 * add - to sum numbers
 * @a: the first number
 * @b: the second number
 * Return: to return sum
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - to minus numbers
 * @a: the first number
 * @b: the second number
 * Return: to return sub
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mod - to get mod numbers
 * @a: the first number
 * @b: the second number
 * Return: to return mod
 */
int mod(int a, int b)
{
	return (a % b);
}

/**
 * mul - to multiply numbers
 * @a: the first number
 * @b: the second number
 * Return: to return multiplication
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - to divide numbers
 * @a: the first number
 * @b: the second number
 * Return: to return division
 */
int div(int a, int b)
{
	return (a / b);
}
